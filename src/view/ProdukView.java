    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            namaProduk = tfNamaProduk.getText();
            hargaProduk = tfHargaProduk.getText();
            stokProduk = tfStok.getText();

            Produk p = new Produk();
            p.setId_produk(idProduk);
            p.setHarga(Double.parseDouble(hargaProduk));
            p.setNama_produk(namaProduk);
            p.setStok(Integer.parseInt(stokProduk));
            this.produkctrl.insertProduct(p);
            this.getDataProduk();

            // Reset Field
            tfNamaProduk.setText("");
            tfHargaProduk.setText("");
            tfStok.setText("");

        } catch (SQLException ex) {
            Logger.getLogger(ProdukView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

