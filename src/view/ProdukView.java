    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int col = 0;
        int row = tblProduk.getSelectedRow();
        System.out.println("ROW = " + row);
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Produk belum dipilih, Pilih produk terlebih dahulu !");
        } else {

            try {
                String id = tblProduk.getModel().getValueAt(row, col).toString();
                System.out.println("ID = " + id);
                this.produkctrl.deleteProduk(Integer.parseInt(id));
                this.getDataProduk();
            } catch (SQLException ex) {
                Logger.getLogger(PegawaiView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int col = 0;
        int row = tblProduk.getSelectedRow();
        System.out.println("ROW = " + row);
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Produk belum dipilih, Pilih produk terlebih dahulu !");
        } else {
            String id = tblProduk.getModel().getValueAt(row, col).toString();
            System.out.println("ID = " + id);
            try {
                Produk p = new Produk();
                p.setId_produk(Integer.parseInt(id));
                Produk produk = this.produkctrl.getDataproduk(p).get(0);
                idProduk = produk.getId_produk();
                tfNamaProduk.setText(produk.getNama_produk().toString());
                tfHargaProduk.setText(String.valueOf(produk.getHarga()));
                tfStok.setText(produk.getStok().toString());
                btnSimpan.setText("Update");
            } catch (SQLException ex) {
                Logger.getLogger(PegawaiView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

