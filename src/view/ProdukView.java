    public void getDataProduk() throws SQLException {
        DefaultTableModel dtmProduk = new DefaultTableModel(new String[]{"ID","Nama Produk", "Harga ", "Stok"}, 0);
        dtmProduk.setRowCount(0);
        for (Produk p : this.produkctrl.getDataproduk(null)) {
            dtmProduk.addRow(new String[]{p.getId_produk().toString() ,p.getNama_produk().toString(), String.valueOf(p.getHarga()), String.valueOf(p.getStok())});
        }
        tblProduk.setModel(dtmProduk);
    }

