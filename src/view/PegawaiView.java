    public void getDataPegawai() throws SQLException {
        DefaultTableModel dtmPegawai = new DefaultTableModel(new String[]{"Id", "Nama", "Alamat"}, 0);
        dtmPegawai.setRowCount(0);
        for (Pegawai p : this.pegawaiCtrl.getDataPegawai(null)) {
            dtmPegawai.addRow(new String[]{p.getId_pegawai().toString(), p.getNama_Pegawai().toString(), p.getAlamat().toString()});
        }

        tblPegawai.setModel(dtmPegawai);
    }

