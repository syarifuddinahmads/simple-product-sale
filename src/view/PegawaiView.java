    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        try {
            namaPegawai = tfNama.getText().toString();
            alamatPegawai = taAlamat.getText().toString();
            Pegawai p = new Pegawai();
            p.setId_pegawai(idPegawai);
            p.setNama_Pegawai(namaPegawai);
            p.setAlamat(alamatPegawai);
            this.pegawaiCtrl.insertPegawai(p);
            this.getDataPegawai();
            // Reset Field
            tfNama.setText("");
            taAlamat.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(PegawaiView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int col = 0;
        int row = tblPegawai.getSelectedRow();
        System.out.println("ROW = " + row);
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Pegawai belum dipilih, Pilih pegawai terlebih dahulu !");
        } else {
            String id = tblPegawai.getModel().getValueAt(row, col).toString();
            System.out.println("ID = " + id);
            try {
                Pegawai p = new Pegawai();
                p.setId_pegawai(Integer.parseInt(id));
                Pegawai pegawai = this.pegawaiCtrl.getDataPegawai(p).get(0);
                idPegawai = pegawai.getId_pegawai();
                tfNama.setText(pegawai.getNama_Pegawai().toString());
                taAlamat.setText(pegawai.getAlamat().toString());
                btnSimpan.setText("Update");
            } catch (SQLException ex) {
                Logger.getLogger(PegawaiView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int col = 0;
        int row = tblPegawai.getSelectedRow();
        System.out.println("ROW = " + row);
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Pegawai belum dipilih, Pilih pegawai terlebih dahulu !");
        } else {

            try {
                String id = tblPegawai.getModel().getValueAt(row, col).toString();
                System.out.println("ID = " + id);
                this.pegawaiCtrl.deletePegawai(Integer.parseInt(id));
                this.getDataPegawai();
            } catch (SQLException ex) {
                Logger.getLogger(PegawaiView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

